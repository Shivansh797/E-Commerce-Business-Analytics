def Customer_Analysis():
    file14=pd.merge(file1,file4,on="customer_id",how="inner")
    sns.set_style("whitegrid")
    count1=file14["customer_city"].value_counts().head(10).index
    sns.countplot(data=file14[file14["customer_city"].isin(count1)],y="customer_city",hue="customer_city",palette="viridis",legend=False)
    plt.xlabel("No. Of Orders Placed", fontweight="bold")
    plt.ylabel("Customer City", fontweight="bold")
    plt.title("Top 10 City With Highest No. Of Orders Placed", fontweight="bold")
    plt.savefig(os.path.join(save,"Top_10_city_orders.png"),dpi=300)
    plt.show()
    count2=file14["customer_state"].value_counts().head(10).index
    sns.countplot(data=file14[file14["customer_state"].isin(count2)],y="customer_state",hue="customer_state",palette="viridis",legend=False)
    plt.xlabel("No. Of Orders Placed", fontweight="bold")
    plt.ylabel("Customer State", fontweight="bold")
    plt.title("Top 10 State With Highest No. Of Orders Placed", fontweight="bold")
    plt.savefig(os.path.join(save,"Top_10_state_orders.png"),dpi=300)
    plt.show()
    not_delivered = file14[file14["order_status"] != "delivered"]
    count3= not_delivered["customer_city"].value_counts().head(5).index.tolist()
    sns.countplot(data=not_delivered[not_delivered["customer_city"].isin(count3)],x="order_status",hue="customer_city",order=not_delivered["order_status"].value_counts().index)
    plt.xlabel("Order Status", fontweight="bold")
    plt.ylabel("No. Of Orders", fontweight="bold")
    plt.legend(title="City")
    plt.title("Top 5 Cities With Orders Not Yet Delivered", fontweight="bold")
    plt.savefig(os.path.join(save,"Top_10_city_not_delivered.png"),dpi=300)
    plt.show()
