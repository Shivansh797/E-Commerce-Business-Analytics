def Review_Analysis():
    file257 = pd.merge(file25, file7, on="order_id", how="inner")
    file2573 = pd.merge(file257, file3, on="seller_id", how="inner")
    sns.set_style("whitegrid")
    top10 = file2573["seller_city"].value_counts().head(10).index
    f1 = file2573[file2573["seller_city"].isin(top10)]
    f1["average_review"] = f1.groupby("seller_city")["review_score"].transform("mean")
    averagereview = (f1.groupby("seller_city")["average_review"].mean().sort_values(ascending=False).head(10).reset_index())
